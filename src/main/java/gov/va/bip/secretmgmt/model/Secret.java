package gov.va.bip.secretmgmt.model;

public class Secret {
    private String key;
    private String value;

    public Secret() {}

    public Secret(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}